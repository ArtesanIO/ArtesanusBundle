<?php

namespace ArtesanIO\ArtesanusBundle\Utils;

class Slugger
{
    public function slugify($string)
    {
        $string = str_replace(' ', '-', strtolower(trim(strip_tags($this->stripAccents($string)))));

        return $string;
    }

    function stripAccents($string) {

        $no_permitted= array ("á","é","í","ó","ú","Á","É","Í","Ó","Ú","ñ","À","Ã","Ì","Ò","Ù","Ã™","Ã ","Ã¨","Ã¬","Ã²","Ã¹","ç","Ç","Ã¢","ê","Ã®","Ã´","Ã»","Ã‚","ÃŠ","ÃŽ","Ã”","Ã›","ü","Ã¶","Ã–","Ã¯","Ã¤","«","Ò","Ã","Ã„","Ã‹");
        $permitted= array ("a","e","i","o","u","A","E","I","O","U","n","N","A","E","I","O","U","a","e","i","o","u","c","C","a","e","i","o","u","A","E","I","O","U","u","o","O","i","a","e","U","I","A","E");

        return str_replace($no_permitted, $permitted ,$string);
    }
}

